def main() -> None:
    preserve = {"stylesheets", "javascripts", "assets", "images"}
    if DOCS.exists():
        for p in list(DOCS.iterdir()):
            if p.name in preserve:
                continue
            if p.is_file():
                p.unlink()
            else:
                for child in p.rglob("*"):
                    if child.is_file():
                        child.unlink()
                for child in sorted(p.rglob("*"), reverse=True):
                    if child.is_dir():
                        child.rmdir()
                p.rmdir()
    DOCS.mkdir(parents=True, exist_ok=True)
    (DOCS / "stylesheets").mkdir(exist_ok=True)
    (DOCS / "javascripts").mkdir(exist_ok=True)

    write_home()

    for part in PARTS:
        part_dir = DOCS / part["slug"]
        part_dir.mkdir(parents=True, exist_ok=True)
        (part_dir / "index.md").write_text(render_part_index(part), encoding="utf-8")
        for ch in part["chapters"]:
            (part_dir / ch["file"]).write_text(
                render_chapter(part["title"], ch), encoding="utf-8"
            )

    appendix_dir = DOCS / "appendix"
    appendix_dir.mkdir(parents=True, exist_ok=True)
    for item in APPENDIX:
        (appendix_dir / item["file"]).write_text(render_appendix(item), encoding="utf-8")

    nav_path = ROOT / "nav.generated.yml"
    nav_path.write_text(build_nav_yaml(), encoding="utf-8")
    print(f"Generated docs under {DOCS}")
    print(f"Nav snippet written to {nav_path}")


if __name__ == "__main__":
    main()
